/// A plant created offline has no server-assigned revision yet. `0` is below
/// the contract's `Revision` minimum of `1`
/// (`packages/api-contracts/openapi.yaml`), so it can never be mistaken for a
/// real server revision — the exact sentinel and reasoning
/// `FeatureGardens.unconfirmedGardenRevision`/`FeatureMap`'s identical
/// `revision: 0` treatment already establish. `Plant.revision` stays a plain
/// `Int` for the same "not worth threading `Int?` through the whole feature
/// for this one local-only case" reasoning `unconfirmedGardenRevision`'s own
/// doc comment gives.
private let unconfirmedPlantRevision = 0

/// The contract's shared length rule for a plant's display name
/// (`packages/api-contracts/openapi.yaml`, `AddPlantRequest.displayName` /
/// `UpdatePlantDetailsRequest.displayName`, `minLength: 1, maxLength: 200`) —
/// also the backend's own `validateDisplayName`
/// (`plants-inventory/domain/plant.ts`). Not previously enforced client-side
/// (the "Add a plant" form's own `AddPlantFormValidation` only checked
/// non-empty); step 2 of the local mutation transaction (architecture/
/// offline-synchronization.md, section "6") is where the upper bound belongs
/// now that an offline command has nowhere else to be checked before it is
/// queued — the same "previously-unenforced contract limit, wired in now"
/// pattern `FeatureGardens.gardenNameMaxLength`'s own doc comment describes.
private let plantDisplayNameMaxLength = 200

private func validatedDisplayName(_ displayName: String) throws -> String {
    let trimmed = displayName.trimmingCharacters(in: .whitespacesAndNewlines)

    guard !trimmed.isEmpty, trimmed.count <= plantDisplayNameMaxLength else {
        throw PlantCommandError.invalidDisplayName
    }

    return trimmed
}

/// Applies a `FieldUpdate` to a current optional value: `.unchanged` keeps
/// `current`, `.set(value)` (including `.set(nil)`, which clears it) becomes
/// the new value — mirrors the backend's own `changes.field !== undefined ?
/// ... : plant.field` handling in `updatePlantDetails`
/// (`plants-inventory/domain/plant.ts`).
private func applying<Value>(_ update: FieldUpdate<Value>, to current: Value?) -> Value? {
    switch update {
    case .unchanged: current
    case let .set(value): value
    }
}
